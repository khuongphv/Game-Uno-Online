    public void receivedDataProcessing(ObjectWrapper data){
        if(data.getData() instanceof ArrayList<?>){
            listPlayer = (ArrayList<Player>)data.getData();
            Collections.sort(listPlayer,Player.sortbyWinrate);
        }
        getRank();
    }
