    private void getRank(){
        model.setRowCount(0);
        Collections.sort(listPlayer, Player.sortbyWinrate);
        int count = 1;
        for(Player i: listPlayer){
            model.addRow(new Object[]{count++, i.getUsername(),i.getWinrate(),i.getTotalmatch()});
        }
    }
    
