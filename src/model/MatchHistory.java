package model;

import java.io.Serializable;

/**
 *
 * @author fake1
 */
public class MatchHistory implements Serializable{
    private int id;
    private int status;
    private String begin, end; //time

    public MatchHistory() {
    }

    public MatchHistory(int id, int status, String begin, String end) {
        this.id = id;
        this.status = status;
        this.begin = begin;
        this.end = end;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getBegin() {
        return begin;
    }

    public void setBegin(String begin) {
        this.begin = begin;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }
    
}
